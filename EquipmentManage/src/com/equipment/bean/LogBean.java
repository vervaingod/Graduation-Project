package com.equipment.bean;

public class LogBean {
	private int log_id;
	private String log_operation;
	private String log_content;
	private String log_people;
	private String log_time;
	private int log_maintenance_id;

	public int getLog_id() {
		return log_id;
	}

	public void setLog_id(int log_id) {
		this.log_id = log_id;
	}

	public String getLog_operation() {
		return log_operation;
	}

	public void setLog_operation(String log_operation) {
		this.log_operation = log_operation;
	}

	public String getLog_content() {
		return log_content;
	}

	public void setLog_content(String log_content) {
		this.log_content = log_content;
	}

	public String getLog_people() {
		return log_people;
	}

	public void setLog_people(String log_people) {
		this.log_people = log_people;
	}

	public String getLog_time() {
		return log_time;
	}

	public void setLog_time(String log_time) {
		this.log_time = log_time;
	}

	public int getLog_maintenance_id() {
		return log_maintenance_id;
	}

	public void setLog_maintenance_id(int log_maintenance_id) {
		this.log_maintenance_id = log_maintenance_id;
	}

}
